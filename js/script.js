$(document).ready(function () {

    $('.slick-carousel').slick({
        arrows: false,
        autoPlay: true,
        autoplaySpeed: 1000
    });

    $(window).scroll(function () {
        if ($(this).scrollTop() > 100) {
            $('.scrollup').fadeIn();
        } else {
            $('.scrollup').fadeOut();
        }
    });
    $('.scrollup').click(function () {
        $("html, body").animate({
            scrollTop: 0
        }, 600);
        return false;
    });


});
